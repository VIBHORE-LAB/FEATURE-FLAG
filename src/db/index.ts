import pkg from 'pg';
const {Pool} = pkg;

let pool: any;

export async function initDB() {
    pool = new Pool({
        connectionString: process.env.DATABASE_URL
    });

    await pool.query('SELECT 1');
    console.log('DB connected')
}

export function getDB(){
    if(!pool){
        throw new Error('DB not initialized');
    }

    return pool;
}